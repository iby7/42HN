int ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int k;

	k = 0;
	if (n == 0)
		return 0;
	while(k < n && *s1 && (*s1 == *s2) )
	{
		s1++;
		s2++;
		k++;
	}
	return(*s1 - *s2);
}
